let FIREWORKS = []
let CX, CY
const NFW = 7

function setup() {
  createCanvas(400, 400)
  CX = CY = 0
  for (let i = 0; i < SHAPE.length; i++){
	  CX += SHAPE[i][0] / SHAPE.length
	  CY += SHAPE[i][1] / SHAPE.length
  }
  for (let i = 0; i < NFW; i++)
    FIREWORKS.push(newFW())
}

function draw() {
  background(9, 14, 49, 11)
  for (let i = 0; i < FIREWORKS.length; i++){
    if (FIREWORKS[i].update().show())
      FIREWORKS[i] = newFW()
  }
}

function newFW(){
  return new Firework(randomGaussian(width/2,width/4), random(height-10, height), random(80), random(TAU), random(100, 200))
}

class Firework{
  constructor(x, y, s, skew, duration){
    this.x = x
    this.y = y
    this.size = s
    this.s = 0
    this.skew = skew
    this.t = duration
  }
  update(){
    if (--this.t > 0){
      this.x += random(-1,1)
      this.y -= random(5)
    }else{
      this.s += 1
    }
    return this
  }
  show(){
      if (this.s < 1){
        strokeWeight(6)
        stroke(255, 255, 255, 155)
        point(this.x, this.y)
        
      }else{
      push()
      translate(this.x, this.y)
      rotate(this.skew)
      for(let i = 0; i < SHAPE.length; i++){
        stroke(255, random(255), random(255), 255-this.s)
        strokeWeight(random(9))
		  const X = SHAPE[i][0] - CX
		  const Y = SHAPE[i][1] - CY
		  const D = mag(X, Y)
		  const T = atan2(Y, X)
		  const R = min(D, -D*this.t/40)
        point(R*cos(T), R*sin(T))
      }
    pop()
    }
    if(this.s > this.size)
      return true
    else
      return false
  }
    
}

